    use HasRoles;
    use Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name', 'email', 'password', 'is_admin', 'is_staff', 'is_student', 'staff_type',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */

        'password', 'remember_token',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    public function dep() {
        return $this->hasone('App\deps');
    }

    public function branch() {
        return $this->belongsTo('App\branch', 'branch_id');
    }



